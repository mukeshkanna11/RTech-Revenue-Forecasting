/* =========================================
   DOWNLOAD INVOICE PDF
========================================= */

exports.downloadInvoicePDF = async (req, res) => {
  try {
    const { id } = req.params;

    // Fetch invoice
    const invoice = await exports.getInvoiceById(id);

    // Generate PDF
    const element = React.createElement(InvoicePDF, { data: invoice });
    const stream = await ReactPDF.renderToStream(element);

    // Set headers for PDF download
    res.set({
      "Content-Type": "application/pdf",
      "Content-Disposition": `attachment; filename=${invoice.invoiceNumber || "invoice"}.pdf`
    });

    // Pipe PDF to response
    stream.pipe(res);

  } catch (err) {
    console.error("PDF DOWNLOAD ERROR:", err);
    res.status(500).json({
      success: false,
      message: err.message || "Internal Server Error"
    });
  }
};